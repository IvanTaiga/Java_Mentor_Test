package calculate;

// Класс, отвечающие за арифметические операции

public class Calculation {

    public static int sum(int value1, int value2) {

        int intResult = value1 + value2;

        return intResult;
    }

    public static int deduct(int value1, int value2) {


        int intResult = value1 - value2;

        return intResult;
    }

    public static int multi(int value1, int value2) {

        int intResult = value1 * value2;

        return intResult;
    }

    public static int div(int value1, int value2) {

        int intResult = value1 / value2;

        return intResult;
    }

}
